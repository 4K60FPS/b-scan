<h1>Introducere</h1>

<p>B-SCAN* a fost conceput pentru scanarea vulnerabilitatilor din serverele care au la baza distributia LINUX. B-SCAN se folosete de scanarea de tip <b>bruteforce</b> pentru a incerca orice combinatii generale.</p>

<p>Pe langa acest fapt, un sistem de login si register a fost pus la dispozitie pentru a oferi acces mai multor persoane in timp real.</p>
<sub>* Proiectul a fost facut doar pentru scopuri eduactionale, folosirea lui in alte scopuri se pedepseste conform termenelor si conditiilor impuse de tara respectiva si/sau providerul de gazduire.</sub>

<h1>Exemple din cod</h1>

<p>Functiile care preiau date se pot gasi in folderul ScannerAPI</p>

<h1>Motivare</h1>

<p>Initial, am pornit de la un proiect mai vechi cu care am castigat premiul I la Concursul Visual Contest care a fost organizat la Colegiul Tehnic "Radu Negru" din Galati, respectiv un proiect intitulat PLUS-MINUS IT care avea ca scop demonstrarea unui atac DDoS trimis din pagina web, iar treptat a ajuns o platforma pentru scanarea vulnerabilitatilor.</p>

<h1>Instalare</h1>

<p>Pentru instalarea API-ului puteti gasi informatii in folderul ScannerAPI. Acest API poate asigura mai multe conexiuni cu serverul principal</p>
<p>Pentru instalarea Platformei B-SCAN trebuie sa dispuneti de o baza de date MySQL si fisierele respective prezente in directorul principal</p>

